# Pick up on keywords and name fragments that might look like a "procedure" name but aren't.
sub isCobolKeyWord {
	my ($name) = @_;
	$name = uc($name);
	my $result = defined($CobolKW{$name}) ? 1 : 0;

	return ($result);
}

